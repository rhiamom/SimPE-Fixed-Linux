// Local shim for SimPe.Helper.LoadImage, which lives in SimPE.Helper.dll —
// a project that NetDocks does not reference.
// The original code called Helper.LoadImage(stream) to load embedded PNGs via
// System.Drawing.Image.FromStream.

namespace Ambertation.Windows.Forms;

internal static class Helper
{
    public static System.Drawing.Image LoadImage(System.IO.Stream stream)
        => stream == null ? null : System.Drawing.Image.FromStream(stream);
}
